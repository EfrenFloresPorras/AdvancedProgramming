// Ejemplo de tipos de datos en c++

#include <iostream>
using namespace std;

int main(int args, const char *argv[]){
    // Tipos de datos
    int a = 1;
    float b = 1.1;
    double c = 1.2;
    char d = 'a';
    bool e = true;
    string f = "Hello World!";
    // Imprimir en consola
    cout << "int: " << a << endl;
    cout << "float: " << b << endl;
    cout << "double: " << c << endl;
    cout << "char: " << d << endl;
    cout << "bool: " << e << endl;
    cout << "string: " << f << endl;
    return 0;
}